    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //INSTANCIAR DAO E MODEL 
        try {
            Produto prod = new Produto();
            prod.setNome(jTextField1.getText());
            prod.setPreco(Double.parseDouble(jTextField2.getText()));
            prod.setImagem(jTextField3.getText());
            ProdutoDao prodDao = new ProdutoDao();
            prodDao.adiciona(prod);
            this.dispose();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Cadastrar.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "O valor posto no preco não é valido");
            Logger.getLogger(Cadastrar.class.getName()).log(Level.SEVERE, null, e);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

