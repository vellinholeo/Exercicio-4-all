    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        JFileChooser chooser = new JFileChooser();
        chooser.setCurrentDirectory(new java.io.File("C:\\Users\\vellinholeo\\Pictures"));
        chooser.setVisible(true);
        chooser.setDialogType(JFileChooser.OPEN_DIALOG);
        chooser.setDialogTitle("Abrir Arquivo");
        FileNameExtensionFilter filter = new FileNameExtensionFilter("JPG e PNG", new String[]{"jpg", "png"});
        chooser.setFileFilter(filter);
        chooser.showDialog(this, null);
        jTextField3.setText(chooser.getSelectedFile().toString());
    }//GEN-LAST:event_jButton3ActionPerformed

